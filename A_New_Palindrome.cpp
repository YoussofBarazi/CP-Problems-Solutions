const int N = 200009;

void solve()
{
    string s;
    cin >> s;

    vector<int> freq(28, 0);

    int cnt = 0;
    for (size_t i = 0; i < s.size(); i++)
    {
        freq[s[i] - 'a']++;
    }

    for (auto x : freq)
        cnt += x > 1;

    if (cnt >= 2)
        cout << "YES" << endl;
    else
        cout << "NO" << endl;
}

