using namespace std;

const int N = 200009;

void solve()
{
    int n;
    cin >> n;

    ll ans = 1, mod = 1e9 + 7;
    for (ll i = 2; i <= n; i++)
        ans *= i, ans %= mod;

    ll x = 1;
    for (int i = 1; i < n; i++)
        x *= 2, x %= mod;

    ans -= x;
    ans += mod;
    ans %= mod;

    cout << ans << endl;
}

int main()
{
    IOS;

    int t = 1;

    while (t--)
    {
        solve();
    }

    return 0;
}