const int N = 200009;

void solve()
{
    int n;
    cin >> n;

    vector <int> a(n);
    for (auto &x : a)
        cin >> x;

    sort(all(a));

    int i = a[0] , j = a[n - 1] , k = a[1] , l = a[n - 2];

    int ans = abs(j - i) + abs(j - k) + abs(k - l) + abs(l - i);

    cout << ans << endl;
}

