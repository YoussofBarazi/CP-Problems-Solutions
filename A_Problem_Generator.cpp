void solve()
{
    int n , m;
    cin >> n >> m;
    string s;
    cin >> s;

    map <char , int> freq;

    for (auto x : s)
        freq[x] ++;

    int ans = 0;

    for (char c = 'A' ; c <= 'G' ; c ++)
    {
        if (freq[c] >= m)
            continue;
        
        ans += m - freq[c];
    }

    cout << ans << endl;
}

