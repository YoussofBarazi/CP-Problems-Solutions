using namespace std;

const int N = 200009;

void solve()
{
    int n , m;
    cin >> n >> m;

    ll ans = 0;
    for (int i = 1 ; i <= 5 ; i ++)
        ans += 1ll * (n / 5 + (n % 5 >= i)) * (m / 5 + (m % 5 >= (5 - i) && 5 != i));

    cout << ans << endl;
}

int main()
{
    IOS;

    int t = 1;

    while (t--)
    {
        solve();
    }

    return 0;
}